package cmm02.op;

public class No01_DanHangDemo {

	public static void main(String[] args) {
		int i = 5 ;
		i = i + 1 ;
		System.out.println("i = i +1 의 결과 값 : "+ i);
		
		int j = 5;
		j++;  // 호출 된 후의 증감 
		System.out.println("j++ 의 결과 값 : "+j);
		
		
		
		int k = 5;
		++k ; // 호출 되기전의 증감 // 이미 증가 된 후의 값
		System.out.println("++k 의 결과 값 : "+k);
		
		
		
	}
	
	
}
